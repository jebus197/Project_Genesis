"""Landing page — the social home feed."""

from fastapi import APIRouter, Request
from datetime import datetime, timezone
from decimal import Decimal

from genesis.web.deps import get_service, get_templates
from genesis.web.negotiate import respond

router = APIRouter()


@router.get("/")
async def landing(request: Request):
    service = get_service()
    templates = get_templates(request)
    status = service.status()
    listings_result = service.search_listings(limit=4)
    first_light = _first_light_projection(service)
    front_page = {
        "headline_stats": {
            "open_listings": status.get("market", {}).get("open_listings", 0),
            "total_bids": status.get("market", {}).get("total_bids", 0),
            "committed_epochs": status.get("epochs", {}).get("committed", 0),
            "active_humans": status.get("actors", {}).get("humans", 0),
        },
        "first_light": first_light,
        "epoch_current": {
            "name": "Epoch 1 - Foundation",
            "rationale": (
                "Genesis is validating trust, governance, mission settlement, "
                "and audit reliability under real operational pressure."
            ),
        },
        "epoch_next": {
            "name": "Epoch 2 - Distributed Compute",
            "trigger": (
                "Activates when verified participation and available distributed "
                "compute reach the published threshold model."
            ),
        },
        "signals": _front_page_signals(),
    }
    context = {
        "request": request,
        "active_tab": "home",
        "status": status,
        "listings": listings_result.data.get("listings", []),
        "front_page": front_page,
    }
    return respond(request, templates, "home_feed.html", context)


def _first_light_projection(service) -> dict:
    """Build a deterministic First Light projection snapshot for PoC front page."""
    now = datetime.now(timezone.utc)
    result = service.check_first_light(
        monthly_revenue=Decimal("5200"),
        monthly_costs=Decimal("9800"),
        reserve_balance=Decimal("12400"),
        missions_per_human_per_month=1.7,
        avg_mission_value=Decimal("680"),
        commission_rate=Decimal("0.05"),
    )
    data = result.data if result.success else {}

    achieved = bool(data.get("first_light"))
    progress_pct = float(data.get("progress_pct", 100.0 if achieved else 0.0))
    estimated_iso = data.get("estimated_date")
    estimated_utc = None
    if estimated_iso:
        try:
            estimated_utc = datetime.fromisoformat(estimated_iso.replace("Z", "+00:00"))
        except ValueError:
            estimated_utc = None

    countdown = None
    if estimated_utc and estimated_utc > now:
        total_minutes = int((estimated_utc - now).total_seconds() // 60)
        days = total_minutes // (24 * 60)
        hours = (total_minutes % (24 * 60)) // 60
        minutes = total_minutes % 60
        countdown = {"days": days, "hours": hours, "minutes": minutes}

    return {
        "achieved": achieved,
        "progress_pct": progress_pct,
        "estimated_iso": estimated_utc.isoformat() if estimated_utc else None,
        "estimated_label": (
            estimated_utc.strftime("%d %b %Y") if estimated_utc else "Projection pending"
        ),
        "message": data.get("message", "Projection pending live mission economics."),
        "countdown": countdown,
    }


